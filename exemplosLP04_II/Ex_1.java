public class Ex_1 {
    public static void main(String[] args) {
        float n1, n2, som, sub, mult, div;
        n1 = 10;
        n2 = 4;
        
        som = n1+n2;
        sub = n1-n2;
        mult = n1*n2;
        div = n1/n2;
        
        System.out.println("Soma = "+som);
        System.out.println("Subtração = "+sub);
        System.out.println("Multiplicação = "+mult);
        System.out.println("Divisão = "+div);
        
    }
}
